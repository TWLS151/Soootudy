T = int(input())

for tc in range(1, T+1):
    # 현재 전구 상태 입력 (리스트로 바꿔야 상태를 직접 수정할 수 있음)
    status = list(input())   
    cnt = 0

    # 전구를 1번부터 차례대로 확인
    for i in range(len(status)):    
        if status[i] == 'Y':    # 'Y'이면 숫자 세기
            cnt += 1
        
            # 이 부분을 오래 고민하다가.. 약간의 ai 힌트를 얻음.. 
            # i+1번 스위치는 (i+1)의 배수 전구들을 반전시킴
            # 리스트 인덱스는 0부터 시작하므로 시작점은 i, 간격은 i+1
            for n in range(i, len(status), i+1):    
                if status[n] == 'Y':    # 현재 전구 상태 반전
                    status[n] = 'N'
                else:
                    status[n] = 'Y'
        
    print(f'#{tc} {cnt}')   # 모든 전구를 끄기 위해 누른 스위치 횟수 출력

